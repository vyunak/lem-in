#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	char	*b;

	i = 0;
	if (!(b = ft_strnew(ft_strlen(s1))))
		return (0);
	ft_strcpy(b, s1);
	return (b);
}
