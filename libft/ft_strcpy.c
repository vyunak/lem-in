#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = -1;
	if (src)
	{
		while (src[++i] != '\0')
			dst[i] = src[i];
		dst[i] = '\0';
	}
	return (dst);
}
