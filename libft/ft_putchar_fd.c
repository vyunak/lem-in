#include "libft.h"

void	ft_putchar_fd(int c, int fd)
{
	unsigned char	code[4];

	if (c <= 127 || MB_CUR_MAX != 4)
		code[0] = c;
	else if (c <= 2047)
	{
		code[0] = (c >> 6 & 31) | 192;
		code[1] = (c & 63) | 128;
	}
	else if (c <= 65535)
	{
		code[0] = (c >> 12 & 15) | 224;
		code[1] = (c >> 6 & 63) | 128;
		code[2] = (c & 63) | 128;
	}
	else
	{
		code[0] = (c >> 18 & 7) | 240;
		code[1] = (c >> 12 & 63) | 128;
		code[2] = (c >> 6 & 63) | 128;
		code[3] = (c & 63) | 128;
	}
	write(fd, &code, ft_get_size(c));
}
