#include "libft.h"

void				ft_putnbr(int nb)
{
	unsigned int	i;
	char			x;

	if (nb < 0)
	{
		ft_putchar('-');
	}
	i = nb;
	if (i >= 10)
		ft_putnbr(i / 10);
	x = (i % 10) + '0';
	ft_putchar(x);
}
