#include "libft.h"

int		ft_get_size(int c)
{
	if (c <= 127 || MB_CUR_MAX != 4)
		return (1);
	else if (c <= 2047)
		return (2);
	else if (c <= 65535)
		return (3);
	else
		return (4);
}
